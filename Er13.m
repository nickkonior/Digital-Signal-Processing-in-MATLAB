% Δημιουργούμε τις συμβολικές μεταβλητές
syms z n

% Φτιάχνουμε τους μετασχηματισμούς Ζ
Xz = z / (z - 1/3);
Yz = (z^2 + 1.6*z) / ((z + 1.8)*(0.5*z - 0.8));

% Με την εντολή που μάθαμε στο εργαστήριο φτιάχνουμε τα x[n] και y[n]
x_n = iztrans(Xz);
y_n = iztrans(Yz);

% Υπολογίζουμε τα πρώτα 8 στοιχεία του x[n] και y[n]
x_8 = double(subs(x_n, n, 0:7));
y_8 = double(subs(y_n, n, 0:7));

disp(x_8);
disp(y_8);
