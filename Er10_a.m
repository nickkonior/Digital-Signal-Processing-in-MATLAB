n = -20:20;

% Βάζουμε το σήμα εισόδου
x = 2 * (n == 1) + 4 * (n == -1) + 9 * (n == -2);

% Δεσμεύουμε μνήμη γι την έξοδο 
y = zeros(size(n));

% Φτιάχνουμε τους συντελεστές της ΔΕ 
a1 = -0.4;
b0 = 1; 
b2 = 0.7;

% Εφαρμόζουμε την διαφορική εξίσωση στην έξοδο 
for i = 3:length(n)
    y(i) = b0*x(i) + a1*y(i-1) + b2*x(i-2);
end

stem(n, y);
title('Output');
xlabel('n');
ylabel('y[n]');
